    private void operation(String operation) {
        mode.setText(operation);
        if ("tambah".equals(mode.getText())) {
            btnTambah.setText("Batal");
            btnTambah.setEnabled(true);
            btnSimpan.setEnabled(true);
            btnHapus.setEnabled(false);
            btnUbah.setEnabled(false);
            inputClean();
            inputEnable(true);
        } else if ("tblClick".equals(mode.getText())) {
            System.out.println("operation mode " + mode.getText());
            btnTambah.setText("Tambah");
            btnUbah.setText("Ubah");
            btnTambah.setEnabled(true);
            btnSimpan.setEnabled(false);
            btnHapus.setEnabled(true);
            btnUbah.setEnabled(true);
            inputEnable(false);
        } else if ("ubah".equals(mode.getText())) {
            btnUbah.setText("Batal");
            btnTambah.setEnabled(false);
            btnSimpan.setEnabled(true);
            btnHapus.setEnabled(false);
            btnUbah.setEnabled(true);
            inputEnable(true);
        } else if (mode.getText().isEmpty()) {
            btnTambah.setText("Tambah");
            btnUbah.setText("Ubah");
            btnTambah.setEnabled(true);
            btnSimpan.setEnabled(false);
            btnHapus.setEnabled(false);
            btnUbah.setEnabled(false);
            inputClean();
            inputEnable(false);
        }
    }

    private boolean inputIsEmpty() {
        boolean empty = false;
        if (inputBukuNama.getText().isEmpty() || inputBukuHarga.getText().isEmpty()) {
            empty = true;
        }
        return empty;

    }

    private void inputEnable(boolean x) {
        inputBukuNama.setEnabled(x);
        inputBukuHarga.setEnabled(x);
    }

    private void inputClean() {
        inputBukuId.setText(null);
        inputBukuNama.setText(null);
        inputBukuHarga.setText(null);
    }

