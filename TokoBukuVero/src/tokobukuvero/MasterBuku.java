    private void initTable() {
        tabel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tabel.addColumn("Id");
        tabel.addColumn("Nama");
        tabel.addColumn("Harga");
        tabel.addColumn("Tgl Input");
        tabel.addColumn("Tgl Pembaharuan");
        tblBuku.setModel(tabel);
        tblBuku.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
        tblBuku.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblBuku.getColumnModel().getColumn(1).setPreferredWidth(200);
        tblBuku.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblBuku.getColumnModel().getColumn(3).setPreferredWidth(150);
        tblBuku.getColumnModel().getColumn(4).setPreferredWidth(150);

    }

    private void showTable() {
        tabel.getDataVector().removeAllElements();
        tabel.fireTableDataChanged();
        dataBuku.clear();
        dataBuku = debe.tampil();
        for (int x = 0; x < dataBuku.size(); x++) {
            Object[] data = new Object[9];
            data[0] = dataBuku.get(x).getId();
            data[1] = dataBuku.get(x).getNama();
            data[2] = dataBuku.get(x).getHarga();
            data[3] = dataBuku.get(x).getCreatedAt();
            data[4] = dataBuku.get(x).getUpdatedAt();
            tabel.addRow(data);
        }
    }

